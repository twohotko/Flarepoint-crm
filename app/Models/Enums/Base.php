namespace App\Models\Enums;

/**
 * Class Base
 * @package App\Models\Enums
 */
abstract class Base
{
    /**
     * @return array
     */
    public static function options()
    {
        return [];
    }

    /**
     * @return array
     */
    public static function values()
    {
        $options = static::options();
        return array_keys($options);
    }
}